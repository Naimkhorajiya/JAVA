

class AreaOfRectangle {
    public static void main(String[] args) {
        
        double length = 8.0;
        double width = 5.0;

        double area = length * width;

        System.out.println("Length: " + length);
        System.out.println("Width: " + width);
        System.out.println("Area of Rectangle: " + area);
    }
}
